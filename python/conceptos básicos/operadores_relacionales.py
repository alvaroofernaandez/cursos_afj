#                                 Operadores Relacionales Principales:
#
#                                               1. Igual que (==)
#                                               2. Distinto de (!=)
#                                               3. Menor que (<)
#                                               4. Menor o igual que (<=)
#                                               5. Mayor que (>)
#                                               6. Mayor o igual que (>=)

#1.

r=100 == 100
print(r)

#2.

r=100 != 100
print(r)

#3.

r=99 < 100
print(r)

#4.

r=99 <= 100
print(r)

#5.

r=101 > 99
print(r)

#6.

r=101 >= 99
print(r)
