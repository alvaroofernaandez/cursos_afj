#Tipos de datos.

# Tipo entero:

numero = 50

print("Mi numero favorito es el", numero)
print(type(numero))

# Tipo float:

decimal = 50.6

print("Mi numero decimal favorito es el", decimal)
print(type(decimal))

# Tipo 'string':

cadena = "Hola mundo"

print("Mi cadena favorita es '", cadena, "'")
print(type(cadena))

# Tipo booleano:

dato = True

print("El dato es:", dato)
print(type(dato))

