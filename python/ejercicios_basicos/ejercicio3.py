import math

radio = float(input("Ingresa el valor de radio: "))
area = math.pi * (radio**2)
longitud = 2 * math.pi * radio

print(f"El area del circulo es: {area:.1f} ")
print(f"El longitud del circulo es: {longitud:.1f} ")

# ':.(numero)f' sirve para decidir la cantidad de decimales que se desea usar.
